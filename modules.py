# ─────────────────────────────────────────────────────────────────
# Допоміжні функції
# ─────────────────────────────────────────────────────────────────

def get_module(name: str) -> dict | None:
    """Повертає модуль за назвою або None"""
    return MODULES.get(name.lower())


def list_modules() -> str:
    """Відформатований список модулів для виводу в термінал"""
    lines = ["\n Доступні модулі:\n"]
    for key, mod in MODULES.items():
        lines.append(f"  /module {key:<12} — {mod['label']}")
        lines.append(f"  {'':14}   {mod['desc']}")
        lines.append("")
    return "\n".join(lines)


def default_module() -> dict:
    """Модуль за замовчуванням"""
    return MODULES["ml"]
